    private void txtDelKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDelKeyReleased
        String m = txtDel.getText();
        DefaultTableModel tPaciente= new DefaultTableModel();
        
        tPaciente.addColumn("DUI");
        tPaciente.addColumn("Nombre");
        tPaciente.addColumn("Telefono");
        tPaciente.addColumn("Email");
        tPaciente.addColumn("Edad");
        tPaciente.addColumn("Sexo");
        tPaciente.addColumn("Altura");
        tPaciente.addColumn("Peso");
        tblPaciente.setModel(tPaciente);
        String []datos =new String[8];
        java.sql.Connection con2 = null;
        PreparedStatement pst = null;
        
        try{
            con2 = cn.getConnection();
            pst=con2.prepareStatement("SELECT * FROM `paciente` WHERE dui LIKE'"+(m)+"%' LIMIT 1");
            rs = pst.executeQuery();
            while(rs.next())
            {
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                datos[5]= rs.getString(6);
                datos[6]=rs.getString(7);
                datos[7]=rs.getString(8);
                tPaciente.addRow(datos);
            }
            tblPaciente.setModel(tPaciente);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Ocurrio un error inesperado."+e);
        }
    }//GEN-LAST:event_txtDelKeyReleased

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        java.sql.Connection con2 = null;
        PreparedStatement pst = null;
        
        String m = txtDel.getText();
        
        try{
            con2 = cn.getConnection();
            pst=con2.prepareStatement("DELETE FROM paciente WHERE `paciente`.`dui` = "
                    + "(SELECT dui FROM `paciente` WHERE dui LIKE'"+(m)+"%' LIMIT 1)");
            
            pst.execute();
            txtDel.setText("");
            DefaultTableModel t= new DefaultTableModel();
            t.setRowCount(0);
            tblPaciente.setModel(t);
            JOptionPane.showMessageDialog(this, "Paciente Eliminado con Exito");
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Ocurrio un error inesperado."+e);
        }
    }//GEN-LAST:event_btnDelActionPerformed

