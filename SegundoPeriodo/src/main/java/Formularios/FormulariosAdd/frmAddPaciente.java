    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String dui, nombre, telefono, email, edad, sexo, altura, peso;
        dui =txtDUI.getText();
        nombre = txtNombre.getText();
        telefono = txtTelefono.getText();
        email = txtEmail.getText();
        edad = txtEdad.getText();
        sexo = txtSexo.getText();
        altura = txtAltura.getText();
        peso = txtPeso.getText();
         if(dui.matches("^[0-9]{9}$"))
         {
             if(!edad.equals("") && !nombre.equals("") && !telefono.equals("") && !email.equals("") && !peso.equals("") && !sexo.equals("") && !altura.equals(""))
             {
                 ingresar(dui, nombre, telefono, email, edad, sexo, altura, peso);
                 txtDUI.setText("");
                 txtEdad.setText("");
                 txtNombre.setText("");
                 txtTelefono.setText("");
                 txtEmail.setText("");
                 txtAltura.setText("");
                 txtSexo.setText("");
                 txtPeso.setText("");
             }
             else JOptionPane.showMessageDialog(this, "Ingrese los datos correctamente!!");
         }
         else JOptionPane.showMessageDialog(this, "El DUI deben ser 9 Numeros!!");
    }//GEN-LAST:event_btnAddActionPerformed

