    public void ingresar(String d1, String d2, String d3, String d4, String d5, String d6, String d7, String d8)
    {
        java.sql.Connection con2 = null;
        PreparedStatement pst = null;
        
        try{
            con2 = cn.getConnection();
            pst=con2.prepareStatement("INSERT INTO `paciente` (`dui`, `nombrePaciente`, `telefonoPaciente`, `correoPaciente`, `edadPaciente`, `sexoPaciente`, `alturaPaciente`, `pesoPaciente`) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?,?);");
            pst.setString(1, d1);
            pst.setString(2, d2);
            pst.setString(3, d3);
            pst.setString(4, d4);
            pst.setString(5, d5);
            pst.setString(6, d6);
            pst.setString(7, d7);
            pst.setString(8, d8);
            pst.execute();
            
            JOptionPane.showMessageDialog(this, "Paciente Agregado con Exito");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Ocurrio un error inesperado."+e);
        }
    }
